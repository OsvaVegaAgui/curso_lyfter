def test_bubble_with_small_list():
    #Arrange
    my_test_list = [18, -11, 68, 6, 32, 53, -2]
    
    #Act
    result = bubble_sort(my_test_list)
    
    #Assert
    assert result == [-11, -2, 6, 18, 32, 53,68]
    
